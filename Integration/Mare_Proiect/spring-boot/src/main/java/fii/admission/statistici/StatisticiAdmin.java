public class StatisticiAdmin {
    String numarCandidati;
    String numarVerificate;
    String numarNeverificate;

    public StatisticiAdmin(){

    }

    public String getNumarCandidati() {
        return numarCandidati;
    }

    public void setNumarCandidati(String numarCandidati) {
        this.numarCandidati = numarCandidati;
    }

    public String getNumarVerificate() {
        return numarVerificate;
    }

    public void setNumarVerificate(String numarVerificate) {
        this.numarVerificate = numarVerificate;
    }

    public String getNumarNeverificate() {
        return numarNeverificate;
    }

    public void setNumarNeverificate(String numarNeverificate) {
        this.numarNeverificate = numarNeverificate;
    }


}
